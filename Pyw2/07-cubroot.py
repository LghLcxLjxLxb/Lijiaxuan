# 用牛顿法求解立方根的程序
def Newton_Sqrt(c):
    g = c/2
    i = 0
    while (abs(g*g*g-c) > 0.00000000001):
        i += 1
        g = (2*g+c/g**2)/3
        print("%d:g = %.13f" % (i, g))


n = int(input())
Newton_Sqrt(n)


