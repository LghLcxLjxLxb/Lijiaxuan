# 用牛顿法求解根号2的程序中，把c设为2或2000等不同值
def Newton_Sqrt(c):
    g = c/2
    i = 0
    while (abs(g*g-c) > 0.00000000001):
        i += 1
        g = (g+c/g)/2
        print("%d:g = %.13f" % (i, g))


n = int(input())
Newton_Sqrt(n)

# 当c=2时，值为1.4142135623747
# 当c=9时，值为3.0000000000000
# 当c=10时，值为3.1622776601684
# 当c=99时，值为9.9498743710662
# 当c=999时，值为31.6069612585582
# 当c=2000时，值为44.7213595499958
